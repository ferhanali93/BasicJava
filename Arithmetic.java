public class Arithmetic {
public static void main(String[] args) {
System.out.println("Lets practice Arithmetics");

//Addition
int a = 10;
int b = 15;
System.out.println("The result: " + (a+b));

//Subtraction 
int c = 500;
int d = 300;
System.out.println("The result: " + (c-d));

//Multiplication
int e = 25;
int f = 5;
System.out.println("The result: " + (e*f));

//Division
int g = 100;
int h = 25;
System.out.println("The result: " + (g/h));

//modulus
int x = 25;
int y = 5;
System.out.println("The result: " + (x%y));







}

}
